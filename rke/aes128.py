RCON = [0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36]


def _xtime(a: int) -> int:
    """Multiplication par 2 dans GF(2^8) (réduction par le polynôme AES 0x1B)."""
    return ((a << 1) ^ 0x1B) & 0xFF if a & 0x80 else (a << 1)


def _mul(a: int, b: int) -> int:
    """Multiplication dans GF(2^8) — utilisée par MixColumns."""
    result = 0
    for _ in range(8):
        if b & 1:
            result ^= a
        a = _xtime(a)
        b >>= 1
    return result


def _key_expansion(key: bytes) -> list[list[int]]:
    """Étend la clé 16 octets en 11 sous-clés de tour (16 octets chacune)."""
    words = [list(key[4 * i : 4 * i + 4]) for i in range(4)]
    for i in range(4, 44):
        temp = words[i - 1][:]
        if i % 4 == 0:
            temp = temp[1:] + temp[:1]               # RotWord
            temp = [SBOX[b] for b in temp]            # SubWord
            temp[0] ^= RCON[i // 4 - 1]               # Rcon
        words.append([words[i - 4][j] ^ temp[j] for j in range(4)])
    return [sum((words[4 * r + c] for c in range(4)), []) for r in range(11)]


def _sub_bytes(state: list[int]) -> list[int]:
    return [SBOX[b] for b in state]


def _shift_rows(state: list[int]) -> list[int]:
    out = state[:]
    for r in range(1, 4):
        row = [state[r + 4 * c] for c in range(4)]
        row = row[r:] + row[:r]
        for c in range(4):
            out[r + 4 * c] = row[c]
    return out


def _mix_columns(state: list[int]) -> list[int]:
    out = [0] * 16
    for c in range(4):
        a = state[4 * c : 4 * c + 4]
        out[4 * c + 0] = _mul(a[0], 2) ^ _mul(a[1], 3) ^ a[2] ^ a[3]
        out[4 * c + 1] = a[0] ^ _mul(a[1], 2) ^ _mul(a[2], 3) ^ a[3]
        out[4 * c + 2] = a[0] ^ a[1] ^ _mul(a[2], 2) ^ _mul(a[3], 3)
        out[4 * c + 3] = _mul(a[0], 3) ^ a[1] ^ a[2] ^ _mul(a[3], 2)
    return out


def _add_round_key(state: list[int], rk: list[int]) -> list[int]:
    return [s ^ k for s, k in zip(state, rk)]


def encrypt_block(plaintext: bytes, key: bytes) -> bytes:
    """Chiffre un bloc de 16 octets avec une clé de 16 octets (AES-128, 10 tours)."""
    if len(plaintext) != 16 or len(key) != 16:
        raise ValueError("AES-128 : bloc et clé doivent faire 16 octets")
    rks = _key_expansion(key)
    state = _add_round_key(list(plaintext), rks[0])
    for rnd in range(1, 10):
        state = _add_round_key(_mix_columns(_shift_rows(_sub_bytes(state))), rks[rnd])
    state = _add_round_key(_shift_rows(_sub_bytes(state)), rks[10])
    return bytes(state)
